package com.wraith.enums;

/**
 * Enum representing the status of a wallet.
 *
 * ACTIVE: Wallet is active and can be used
 * INACTIVE: Wallet is temporarily inactive
 * CLOSED: Wallet is permanently closed
 *
 * @author Wallet Service Team
 * @version 1.0.0
 */
public enum WalletStatus {
    ACTIVE,
    INACTIVE,
    CLOSED
}

